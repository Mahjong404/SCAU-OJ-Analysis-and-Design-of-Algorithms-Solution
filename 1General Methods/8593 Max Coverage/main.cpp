//8593 最大覆盖问题
#include <iostream>
#include <algorithm>
#include <cstdlib>
using namespace std;

int a[10005];

int main() {
    int n;
    cin >> n;
    for (int i = 0; i < n; i++) cin >> a[i];

    // 双指针 O(n)：i 和 j 均从末尾出发，只往左走不回退
    int i = n - 1, j = n - 1;
    int maxLen = 0;

    while (i >= 0) {
        if (a[i] <= abs(a[j])) {
            maxLen = max(maxLen, j - i + 1);
            i--;
        } else {
            j--;
        }
    }

    cout << maxLen;
    return 0;
}

/*
Description
给定n个整数a₁, a₂, …, a_n组成的序列。如果对于i≤k≤j，有a_k ≤ |a_j|，
则称a_j覆盖序列区间a_i, a_{i+1}, …, a_j，相应的覆盖区间长度为j-i+1。
最大覆盖问题要求给定序列的最大覆盖区间长度L，即：

L = max { j−i+1 | a_k ≤ |a_j|, i ≤ k ≤ j }
     1≤i≤j≤n

例如，当n=10，相应序列为：1, 6, 2, 1, -2, 3, 5, 2, -4, 3 时，L=5。

输入格式
第1行是正整数n，(n<=10000)
第2行是整数序列 a1 a2 ... an

输出格式
计算出的最大覆盖区间长度。

输入样例
10
1 6 2 1 -2 3 5 2 -4 3

输出样例
5

提示
题目中的样例，结果为5，是因为最大覆盖区间段为：2 1 -2 3 5，因此最大覆盖的区间长度为5。

若依次去求出每个数的最大覆盖长度，则必须有两个嵌套的循环，时间复杂度为O(n²)。
但此处求所有数的一个最大覆盖长度，倒没有必要每个数的最大覆盖长度都求出来。

初始时，用两个指针i和j指向串末，当a_i和a_j的关系满足不等式时，j不动，i往左
走，……，直到不等式不满足，记录下长度。
下一步j往左移一个，i不回退，继续上面的比较，若找到更长的覆盖长度，更新。
每循环一次要么i要么j少1；最后i=-1,j=0；共进行了2(n-1)次。所以时间复杂度为O(n)。
*/
