//11074 平面分割
#include <iostream>
using namespace std;

int main() {
    long long n;
    cin >> n;

    // 直线：L(n) = n(n+1)/2 + 1
    long long L = n * (n + 1) / 2 + 1;
    // 封闭曲线：C(n) = n^2 - n + 2
    long long C = n * n - n + 2;
    // 锯齿形转角折线：Z(n) = 2n^2 - n + 1
    long long Z = 2 * n * n - n + 1;
    // Z字形折线：ZZ(n) = (9n^2 - 7n + 2) / 2
    long long ZZ = (9 * n * n - 7 * n + 2) / 2;

    cout << L << ' ' << C << ' ' << Z << ' ' << ZZ;

    return 0;
}

