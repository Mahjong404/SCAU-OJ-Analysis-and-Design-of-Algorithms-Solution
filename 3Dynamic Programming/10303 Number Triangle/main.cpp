//10303 数字三角（优先做）
#include <iostream>
#include <algorithm>
using namespace std;

int a[105][105], dp[105][105];

int main() {
    int n;
    cin >> n;
    for (int i = 1; i <= n; i++)
        for (int j = 1; j <= i; j++)
            cin >> a[i][j];

    // 自底向上DP
    for (int j = 1; j <= n; j++) dp[n][j] = a[n][j];
    for (int i = n - 1; i >= 1; i--)
        for (int j = 1; j <= i; j++)
            dp[i][j] = max(dp[i + 1][j], dp[i + 1][j + 1]) + a[i][j];

    cout << dp[1][1] << '\n';

    // 回溯路径：靠右优先（dp相等时走右下方）
    int j = 1;
    for (int i = 1; i <= n; i++) {
        cout << a[i][j];
        if (i < n) {
            cout << ' ';
            if (dp[i + 1][j] <= dp[i + 1][j + 1])
                j++;
        }
    }

    return 0;
}

